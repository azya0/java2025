package TSK_201.TSK_201_2;

public class Main {
    public static void main(String[] args) {
        /* 
         * \u0410 — это символ "А".
         * \u0440 — это символ "р".
         * \u0442 — это символ "т".
         * \u0451 — это символ "ё".
         * \u043C — это символ "м".
         */
        String name = "\u0410\u0440\u0442\u0451\u043C"; // Артём
        System.out.println(name);
    }
}
